	EXTRN DIV0:WORD,COUNT:WORD
	PUBLIC WAN2
DATA SEGMENT PARA PUBLIC 'A1'
	SUM DW 0
DATA ENDS

STACK SEGMENT PARA STACK 'STACK'
	DB 200 DUP(0)
STACK ENDS

CODE SEGMENT PARA PUBLIC 'B1'
	ASSUME DS:DATA,CS:CODE,SS:STACK
WAN2 PROC
		MOV AX,0
NEXT: 	ADD AX,BX
		ADD BX,2
		LOOP NEXT
		MOV SUM,AX
		MOV DX,0
		DIV COUNT
		MOV DIV0,AX
		RET
WAN2 ENDP
CODE ENDS
	END